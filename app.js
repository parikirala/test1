// app.js — Simple JavaScript file for Git demo

// Function to greet a user
function greetUser(name) {
    console.log(`Hello, ${name}! Welcome to Git and JavaScript demo.`);
}

// Function to add two numbers
function addNumbers(a, b) {
    return a + b;
}

// Main code execution
const userName = "Vinay";
greetUser(userName);

const sum = addNumbers(10, 20);
console.log(`The sum of 10 and 20 is: ${sum}`);
